var quoteParagraph = document.getElementById('quote');
var quoteAuthor = document.getElementById('author');
var twitterButton= document.getElementById('twitterButton');
var quotesButton= document.getElementById('quotesButton');

// GET THE QUOTE FUNCTION
function uploadAQuote() {
	var num = Math.floor(Math.random() * quotes.length);
	quoteParagraph.innerHTML= '\"' + quotes[num].quote+ '\"';
	quoteAuthor.innerHTML= '- ' + quotes[num].author;
	twitterButton.setAttribute('href','https://twitter.com/intent/tweet?text=' + encodeURIComponent(quoteParagraph.innerHTML) + encodeURIComponent(quoteAuthor.innerHTML));
};

// QUOTES BUTTON
quotesButton.addEventListener('click', function() {
	uploadAQuote();
	quoteParagraph.style.textAlign="justify";
	quoteAuthor.style.textAlign="right";
});
